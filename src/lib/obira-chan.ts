/**
 * キーワードベースのローカル応答（Claude API が使えない環境向けフォールバック）
 * @param input ユーザーの発言
 * @param rand 乱数（テスト時に固定値を注入できるようにする）
 */
export function localReply(input: string, rand: () => number = Math.random): string {
  const text = input.trim();
  if (text === "") {
    return "何も聞こえなかったよ〜？海の中は電波が悪いのかな🐟 もう一回話しかけてみてね。";
  }

  const rule = RULES.find((r) => r.keywords.test(text));
  const replies = rule ? rule.replies : FALLBACK_REPLIES;
  const index = Math.min(Math.floor(rand() * replies.length), replies.length - 1);
  return replies[index];
}
